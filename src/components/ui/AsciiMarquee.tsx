// Simpler fallback for narrow screens
const GOTHIC_ASCII_SIMPLE = `★ GOTHIC is here! ★`;

type AsciiMarqueeProps = {
  className?: string;
  /** Duration of one full pass in seconds */
  duration?: number;
  /** Whether to use the full ASCII art or simple text */
  useFullAscii?: boolean;
};

export default function AsciiMarquee({
  className = "",
  duration = 12,
  useFullAscii = true,
}: AsciiMarqueeProps) {
  const [isVisible, setIsVisible] = useState(false);
  const [useSimple, setUseSimple] = useState(!useFullAscii);

  useEffect(() => {
    // Check screen width for responsive behavior
    const checkWidth = () => {
      setUseSimple(!useFullAscii || window.innerWidth < 768);
    };
    checkWidth();
    window.addEventListener("resize", checkWidth);

    // Small delay before showing to avoid flash
    const timer = setTimeout(() => setIsVisible(true), 100);

    return () => {
      clearTimeout(timer);
      window.removeEventListener("resize", checkWidth);
    };
  }, [useFullAscii]);

  if (!isVisible) return null;

  const text = useSimple ? GOTHIC_ASCII_SIMPLE : GOTHIC_ASCII;

  return (
    <div
      className={`absolute inset-x-0 overflow-hidden pointer-events-none z-20 ${className}`}
      style={{ top: "15%" }}
    >
      <div
        className="inline-block whitespace-pre animate-marquee"
        style={{
          animationDuration: `${duration}s`,
        }}
      >
        <pre
          className={`font-mono ${
            useSimple
              ? "text-2xl md:text-4xl font-bold"
              : "text-[6px] sm:text-[8px] md:text-[10px] lg:text-xs"
          } text-red-500 drop-shadow-[0_0_10px_rgba(220,38,38,0.8)] drop-shadow-[0_0_20px_rgba(185,28,28,0.5)]`}
          style={{
            textShadow: useSimple
              ? "0 0 10px #dc2626, 0 0 20px #dc2626, 0 0 30px #b91c1c, 0 0 40px #7f1d1d"
              : "0 0 5px #dc2626, 0 0 10px #dc2626, 0 0 15px #b91c1c",
          }}
        >
          {text}
        </pre>
      </div>
    </div>
  );
}
